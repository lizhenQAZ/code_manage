#!/bin/bash

:<<!
 1.条件判断
!
[ 1 = 1 ]
echo #?
[ 2 = 1 ]
echo #?

:<<!
 2.逻辑条件
!
[ 1 = 1 ] && echo "条件成立"
[ 2 = 1 ] || echo "条件不成立"

:<<!
 3.文件条件
!
[ -f test.h ] || echo "不是文件" 
[ -d /test ] || echo "不是目录" 
[ -x test.h ] || echo "不是可执行文件"

:<<!
 4.数字条件
!
[ $# -eq 2 ] && echo "$0 脚本开始执行，需要两个参数"
[ $# -ne 2 ] && echo "$0 脚本开始执行，需要不是两个参数"

:<<!
 5.字符串
!
a="$1"
b="$2"
[ "${a}" != "${b}" ] && echo "您输入的两个参数不一样"

